public class 유클리드호제법 {
    /*
    유클리드호제법: 두 수의 최대 공약수를 구하는 알고리즘

    MOD 연산( % )
    : 두 값을 나눈 나머지를 구하는 연산

    1. 큰 수를 작은 수로 나누는 mod 연산 수행
    2. 앞 단계에서의 작은 수와 mod 연산 결과값으로 mod 연산 수행
    3. 2단계를 반복하다가 나머지가 0 이 되는 순간의 작은 수를 최대공약수로 선택한다


    gcd(270. 192)
    270 % 192 = 78
    192 % 78 =36
    78 % 36 = 6
    36 % 6 = 0
    => gcd(270. 192) = 6

     */
}
